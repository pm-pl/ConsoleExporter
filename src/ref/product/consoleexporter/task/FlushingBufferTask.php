declare(strict_types=1);

namespace ref\product\consoleexporter\task;

use pocketmine\scheduler\Task;
use ref\product\consoleexporter\Main;

use function ob_flush;

/** The task that flush frequently to avoid delaying console messages */
class FlushingBufferTask extends Task{
    public function __construct(private Main $plugin){
    }

    public function onRun() : void{
        if($this->plugin->isRecording()){
            ob_flush();
        }
    }
}